async function createManagers() {
  console.log('매니저 100명 생성 시작...');

  let created = 0;
  let skipped = 0;

  for (let i = 0; i < 100; i++) {
    const username = usernames[i];

    // 이미 존재하는지 확인
    const { data: existing } = await supabase
      .from('profiles')
      .select('id')
      .eq('username', username)
      .single();

    if (existing) {
      skipped++;
      continue;
    }

    const profile = {
      email: `${username}@example.com`,
      name: names[i],
      nickname: nicknames[i],
      username: username,
      role: 'manager',
      age: ages[i],
      height: heights[i],
      weight: weights[i],
      body_size: bodySizes[i],
      is_smoker: i % 5 === 0,
      personality: personalities[i % 20],
      style: styles[i % 20],
      skin_tone: skinTones[i % 10],
      hair_length: hairLengths[i % 10],
      hair_style: hairStyles[i % 10],
      is_waxed: i % 3 !== 0,
      hair_color: hairColors[i % 10],
      job: jobs[i % 20],
      mbti: mbtis[i % 20],
      nationalities: nationalities[i % 20],
      languages: languages[i % 20],
    };

    const { error } = await supabase.from('profiles').insert(profile);

    if (error) {
      console.error(`Error creating ${username}:`, error.message);
    } else {
      created++;
      if (created % 10 === 0) {
        console.log(`${created}명 생성 완료...`);
      }
    }
  }

  console.log(`\n완료! 생성: ${created}명, 스킵: ${skipped}명`);
}

createManagers().catch(console.error);
